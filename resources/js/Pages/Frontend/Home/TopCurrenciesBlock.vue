<script>
import FrontendLayout from '@/Layouts/FrontendLayout'
import axios from 'axios'
import Multiselect from '@vueform/multiselect'
import {$vfm, VueFinalModal, ModalsContainer} from 'vue-final-modal'

import {
    getHeaderIcon,
    pluralize,
    pluralize3,
    momentDatetime,
    getErrorMessage,
    getDictionaryLabel,
    formatValue
} from '@/commonFuncs'
import {ref, computed, onMounted} from 'vue'

import {
    settingsJsMomentDatetimeFormat, settingsCurrencyActiveLabels, settingsCurrencyIsTopLabels
} from '@/app.settings.js'
import * as sanitizeHtml from 'sanitize-html';
import JetButton from '@/Jetstream/Button.vue'

export default {
    name: 'TopCurrenciesBlockPage',
    props: {
        show_only_top_currencies: {
            type: Boolean,
            required: false,
            default: false,
        }},

    components: {
        FrontendLayout,
        JetButton,
        // ListingHeader,
        Multiselect,
        VueFinalModal,
        ModalsContainer
    },
    setup(props) {
        console.log('props::')
        console.log(props)

        let show_only_top_currencies = ref(props.show_only_top_currencies)
        let show_currency_history_modal = ref(false)
        let currencyHistoryRows = ref([])
        let currencies_history_pages_count = ref(0)
        let show_currency_description_modal = ref(false)
        let currencyDetails = ref({})
        let currencyDetailsImage = ref({})
        let rate_decimal_numbers = ref(2)
        let baseCurrency = ref({})
        let activeCurrencyRows = ref([])
        let currency_history_id = ref(null)
        let currency_history_char_code = ref('')
        let currency_history_name = ref('')

        let currency_history_current_page = ref(1)
        let main_page_currencies_list_block_header_title = ref('')
        let main_page_currencies_list_block_header_text = ref('')
        let main_page_currencies_list_block_header_image_url = ref('')

        function showCurrencyDetailsModal(currency_id) {
            axios.get(route('frontend.get_currency_details', currency_id))
                .then(({data}) => {
                    console.log('showCurrencyDetailsModal data::')
                    console.log(data)

                    show_currency_description_modal.value = true
                    currencyDetails.value = data.currency
                    currencyDetailsImage.value = data.currencyImage
                })
                .catch(e => {
                    console.error(e)
                })
        }

        function hideCurrencyDescriptionModal() {
            show_currency_description_modal.value = false
        }

        function showCurrencyHistoryModal(currency_id, currency_name, currency_char_code) {
            currency_history_current_page.value =  1
            currency_history_id.value= currency_id
            currency_history_char_code.value= currency_char_code
            currency_history_name.value= currency_name
            loadCurrencyHistory()
        }

        function loadCurrencyHistory() {
            // console.log('loadCurrencyHistory currency_history_id::')
            // console.log(currency_history_id.value)
            // console.log('loadCurrencyHistory currency_history_current_page.value::')
            // console.log(currency_history_current_page.value)

            axios.get(route('frontend.get_currency_history', { id:currency_history_id.value, page:currency_history_current_page.value}))
                .then(({data}) => {
                    console.log('loadCurrencyHistory data::')
                    console.log(data)
                    currencyHistoryRows.value = data
                    show_currency_history_modal.value = true
                    currencies_history_pages_count.value = data.meta.last_page
                })
                .catch(e => {
                    console.error(e)
                })
        }

        function paginateClick(page) {
            currency_history_current_page.value= page
            loadCurrencyHistory()
        }

        function hideCurrencyHistoryModal() {
            show_currency_history_modal.value = false
        }

        function loadActiveCurrencies() {
            let filters = {show_only_top_currencies: show_only_top_currencies.value}
            axios.post(route('frontend.currencies_rates.filter'), filters)
                .then(({data}) => {
                    console.log('loadActiveCurrencies data::')
                    console.log(data)
                    activeCurrencyRows.value = data.data
                })
                .catch(e => {
                    console.error(e)
                })
        } // loadActiveCurrencies() {


        function TopCurrenciesOnMounted() {
            window.emitter.on('listingHeaderRightButtonClickedEvent', params => {
                // console.log('TARGET listingHeaderRightButtonClickedEvent params::')
                // console.log(params)
                if (params.parent_component_key === 'currency') {
                    console.log('!!!!!loadActiveCurrencies::')
                    loadActiveCurrencies()
                }
            })
            //Route::get('/get_base_currency', [HomeController::class, 'get_base_currency'])->name('get_base_currency');

            axios.get(route('get_settings_value', {key: 'rate_decimal_numbers'}))
                .then(({data}) => {
                    rate_decimal_numbers.value = data.value
                })
                .catch(e => {
                    console.error(e)
                })

            axios.get(route('get_base_currency'))
                .then(({data}) => {
                    console.log('get_base_currency data::')
                    console.log(data)

                    baseCurrency.value = data.baseCurrency
                })
                .catch(e => {
                    console.error(e)
                })

            loadActiveCurrencies()
            // showCurrencyHistoryModal(3) // DEBUGGING
            // showCurrencyDetailsModal(3) // DEBUGGING

            axios.get(route('frontend.get_block_cms_item', 'main_page_currencies_list_block_header'))
                .then(({data}) => {
                    // console.log('get_block_cms_item data::')
                    // console.log(data)
                    main_page_currencies_list_block_header_title.value = data.cMSItem.title
                    main_page_currencies_list_block_header_text.value = data.cMSItem.text
                    main_page_currencies_list_block_header_image_url.value = data.image.url
                })
                .catch(e => {
                    console.error(e)
                })


        } // TopCurrenciesOnMounted() {

        onMounted(TopCurrenciesOnMounted)

        return { // setup return
            // Listing Page state
            currency_history_current_page,
            activeCurrencyRows,
            show_only_top_currencies,
            main_page_currencies_list_block_header_title,
            main_page_currencies_list_block_header_text,
            main_page_currencies_list_block_header_image_url,


            // Currency History Modal State
            showCurrencyHistoryModal,
            hideCurrencyHistoryModal,
            show_currency_history_modal,
            paginateClick,
            currencies_history_pages_count,
            currencyHistoryRows,
            show_currency_description_modal,
            currencyDetails,
            currencyDetailsImage,
            currency_history_id,
            currency_history_char_code,
            currency_history_name,
            showCurrencyDetailsModal,
            hideCurrencyDescriptionModal,

            // Page actions
            loadActiveCurrencies,

            // Settings vars
            rate_decimal_numbers,
            baseCurrency,
            settingsJsMomentDatetimeFormat,
            settingsCurrencyActiveLabels,
            settingsCurrencyIsTopLabels,

            // Listing filtering

            // Common methods
            pluralize,
            pluralize3,
            momentDatetime,
            getErrorMessage,
            getHeaderIcon,
            getDictionaryLabel,
            formatValue,
            sanitizeHtml,
        }
    }, // setup() {

}
</script>
