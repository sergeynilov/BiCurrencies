                <div class="p-1 m-0 text-start">

                    <h5 v-if="currency_history_char_code && currency_history_name">
                        <i :class="getHeaderIcon('currencies_history')" class="m-1 p-0 "
                           style="margin-bottom: -2px !important; "></i> {{ currency_history_char_code }} /
                        {{ currency_history_name }} currency history
                    </h5>

